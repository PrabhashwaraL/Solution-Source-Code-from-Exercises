// Exercise 4
// A C program to print 'AGILE' in a reverse order(ELIGA)
#include <stdio.h>

// function main begins program execution
int main(void)
{
    char c1 = 'A';
    char c2 = 'G';
    char c3 = 'I';
    char c4 = 'L';
    char c5 = 'E';

    printf( "The reverse of %c%c%c%c%c is %c%c%c%c%c\n", c1, c2, c3, c4, c5, c5, c4, c3, c2, c1 ); // display result

    return 0;
} // end function main
